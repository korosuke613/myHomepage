                        <h2>よくある質問</h2>
                        <p>よく聞かれる質問とその回答です。新しい質問があったら随時追加します。</p>
                        <div class="panel panel-default">
                            <div class="panel-heading">Q. 門限はありますか？</div>
                            <div class="panel-body">A. ありません。いつでも帰ってこれます。</div>
                        </div>
                        <div class="panel panel-default">
                            <div class="panel-heading">Q. 就寝時刻はありますか？</div>
                            <div class="panel-body">A. ありません。ただし、他の人の迷惑にならないようにしましょう。</div>
                        </div>
                        <div class="panel panel-default">
                            <div class="panel-heading">Q. お風呂はいつでも入れますか？</div>
                            <div class="panel-body">A. 24時間いつでも入れます。</div>
                        </div>
                        <div class="panel panel-default">
                            <div class="panel-heading">Q. 食堂はありますか？</div>
                            <div class="panel-body">A. 補食室で自主的に料理をすることはできますが、いわゆる食堂はありません。しかし、大学の食堂（生協）に徒歩5分で行けます。</div>
                        </div>
                        <div class="panel panel-default">
                            <div class="panel-heading">Q. 盗難はありますか？</div>
                            <div class="panel-body">A.
                                居室にあるものを盗まれたという話を私は聞いたことはありませんが、共用スペースに置いていたもの(料理用品や、冷蔵庫に入れていた飲み物等)が勝手に使われていた、無くなったという話は時々聞きます。中には共用スペースに物を置かないとする人もいます。</div>
                        </div>
                        <div class="panel panel-default">
                            <div class="panel-heading">Q. 寮母さんはいますか？</div>
                            <div class="panel-body">A.
                                基本的に平日の10:00~17:00に寮母さんが1階玄関にいます。寮母さんは寮に関する手続きをしてくれたり、郵便物の受け取りをしてくれたりします。</div>
                        </div>
                        <div class="panel panel-default">
                            <div class="panel-heading">Q. ネット環境はどうなっていますか？</div>
                            <div class="panel-body">A.
                                2017年現在、居室にネット接続をしてくれる業者は3つあります。Iam-net、Sun-Net、フレッツ光の3社です。それぞれの長所短所がありますが、ここに記述することはやめておきます。入寮後に先輩に聞いてください。</div>
                        </div>
                        <div class="panel panel-default">
                            <div class="panel-heading">Q. 上下関係は厳しいですか？</div>
                            <div class="panel-body">A.
                                2017年現在、上下関係はあまり厳しくなく、緩いです。しかし、各階によって多少の上下はあります。上下関係がないという訳ではありませんが、後輩だからという理由で理不尽な目にあうことは滅多にありません。あったとしても、そういうことをする先輩は他の先輩から糾弾されるでしょう。4年生である私がいうとあまり説得力がないかもしれませんが、少なくとも現在の国寮の先輩は優しい先輩しかいません。<br>ただし、1年生には、学祭の準備をしたり、月一のゴミ置き場の掃除をしたりと、一年生特有の責務はあります。</div>
                        </div>
                        <div class="panel panel-default">
                            <div class="panel-heading">Q. 寮生同士の雰囲気はどんな感じですか？</div>
                            <div class="panel-body">A.
                                寮生同士の仲はよい場合がほとんどです。少なくとも顔見知り以上ではあります。廊下で寮生にあったときに世間話が始まったり、談話室で一緒にゲームをしたりと、良い悪いでいうと雰囲気は良いです。</div>
